using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class ScoreScript : MonoBehaviour
{
    private int Total = 0;

    public void ResetScore()
    {
        Total = 0;
    }

    public void TakeHitTarget()
    {
        Total += 100;
    }

    public void TakeHitBalloon()
    {
        Total += 500;
    }

    public int GetTotal()
    {
        return Total;
    }
}
